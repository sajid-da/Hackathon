export type LanguageCode = keyof typeof translations;

export function getTranslation(lang: string): typeof translations.en {
  const normalizedLang = lang.toLowerCase().split('-')[0];
  return translations[normalizedLang as LanguageCode] || translations.en;
}

export function detectLanguageFromText(text: string): LanguageCode {
  const lowerText = text.toLowerCase();
  
  // Kannada detection (Bangalore)
  if (/[р▓Е-р▓╣]/.test(text)) return 'kn';
  
  // Hindi detection
  if (/[рдЕ-рд╣]/.test(text)) return 'hi';
  
  // Tamil detection
  if (/[роЕ-ро╣]/.test(text)) return 'ta';
  
  // Telugu detection
  if (/[р░Е-р░╣]/.test(text)) return 'te';
  
  // Spanish detection
  if (/\b(ayuda|emergencia|necesito|por favor)\b/.test(lowerText)) return 'es';
  
  return 'en';
}
